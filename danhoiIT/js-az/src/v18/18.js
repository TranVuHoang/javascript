/**
 * Kiểu dữ liệu nguyên thuỷ trong JS
 */

// #1. Kiểu Number
const myAge = 29;
myScore = 8.25;
console.log(">>> Check myAge: ", myAge); // 29
console.log(">>> Check myScore: ", myScore); // 8.25
console.log(">>> Check type myScore: ", typeof myScore); // number

// #2. Kiểu string
const name = "Hoàng Trần";
const name1 = "Trang Trang";
const name2 = "Javascript";

console.log(">>> Check name:", name); // "Hoàng Trần"

// #3. Kiểu boolean
const isNumber = true;
console.log(">>> Check isNumber:", isNumber); // true
console.log(">>> Check type isNumber:", typeof isNumber); // boolean

// 4. Kiểu undefined
let bla;
console.log(">>> Check bla: ", bla); // undefined
console.log(">>> Check type bla: ", typeof bla); // undefined

// #5. Kiểu null
let test = null;
console.log(">>> Check test: ", test); // null
console.log(">>> Check type test: ", typeof test); // null
