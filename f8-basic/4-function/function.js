/**
 * Hàm(function) trong JS
 *
 * 1. Hàm?
 * - Một khối mã
 * - Làm 1 việc cụ thể
 *
 * 2. Lọai hàm
 * - Built-in
 * - Tự định nghĩa
 *
 * 3. Tính chất
 * - Không thực thi khi định nghĩa
 * - Sẽ thực thi khi được gọi
 * - Có thể nhận tham số
 * - Có thể trả về 1 giá trị
 *
 * 4. Tạo hàm đầu tiên
 */

// Định nghĩa(tạo) hàm
function showDiaLog() {
  console.log("Hi, xin chào các bạn.");
}

// gọi hàm
showDiaLog();
