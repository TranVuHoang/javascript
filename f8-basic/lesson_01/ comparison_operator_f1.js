/**
 * Toán tử so sánh - comparison operator
 *
 * Toán tử
 * ==     -> Bằng
 * !=     -> Khác
 * >      -> Lớn hơn
 * <      -> Nhỏ hơn
 * >=     -> Lớn hơn hoặc bằng
 * <=     -> Nhỏ hơn hoặc bằng
 */
let a = 1;
let b = 2;
let c = "3";

if (a == b) {
  console.log("Điều kiện đúng.");
} else {
  console.log("Điều kiện sai.");
}

if (a < c) {
  console.log("Điều kiện đúng.");
} else {
  ("Điều kiện sai.");
}
