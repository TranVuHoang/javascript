/**
 * Toán tử số học trong JS
 * +  --> Cộng
 * -  --> Trừ
 * *  --> Nhân
 * /  --> Chia
 * **  --> Luỹ thừa
 * %  --> Chia lấy dư
 * ++  --> Tăng lên 1 giá trị
 * --  --> Giảm đi 1 giá trị
 */
let a = 3;
let b = 2;
let c = a + b;

console.log(">>> check c=", c);
c = a - b;

console.log(">>> check c=", c);

c = a * b;
console.log(">>> check c=", c);

c = a / b;
console.log(">>> check c=", c);

c = a ** b;
console.log(">>> check c=", c);

c = a % b;
console.log(">>> check c=", c);

c = a++;
console.log(">>> check c=", c);

c = a--;
console.log(">>> check c=", c);
